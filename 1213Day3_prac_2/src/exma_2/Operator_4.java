package exma_2;

public class Operator_4 {
	public static void main(String[]args){
		//  > 크다       x > y    변수 x의 값이 변수 y의 값보다 크다.
	    //  < 작다       x < y    변수 x의 값이 변수 y의 값보다 작냐?.

	    //  >= 크거나 같냐       x >= y    변수 x의 값이 변수 y의 값보다 크거나 같냐?.

	    //  <= 크거나 같냐       x <= y    변수 x의 값이 변수 y의 값보다 작거나 같냐?.
		
		//  == 같냐?       x == y    변수 x의 값이 변수 y의 값과 같다.
		
		//  != 같지 않냐?       x != y    변수 x의 값이 변수 y의 값과 같냐?.
		
		
		int a = 10, b = 15;
		
		System.out.println("a =="+a+", b == "+b);
		
		
		System.out.println("a > b :" + (a>b));
		
		System.out.println("a < b :" + (a<b));
		
		System.out.println("a >= b :" + (a>=b));
		
		System.out.println("a <= b :" + (a<=b));
		
		System.out.println("a == b :" + (a==b));
		
		System.out.println("a != b :" + (a!=b));
		
	}
}
