package AllinOne;

public class ExchangeDTO {
	private float USD;
	private float JPY;
	private float EUR;
	public float getUSD() {
		return USD;
	}
	public void setUSD(float uSD) {
		USD = uSD;
	}
	public float getJPY() {
		return JPY;
	}
	public void setJPY(float jPY) {
		JPY = jPY;
	}
	public float getEUR() {
		return EUR;
	}
	public void setEUR(float eUR) {
		EUR = eUR;
	}
	
}
